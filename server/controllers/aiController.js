export const resumeReview = async (req, res) => {
  try {
    const { userId } = req.auth();
    const resume = req.file;
    const plan = req.plan;

    if (plan !== "premium") {
      return res.json({
        success: false,
        message: "This feature is only available for premium subscriptions",
      });
    }

    if (resume.size > 5 * 1024 * 1024) {
      return res.json({
        success: false,
        message: "Resume file size exceeds allowed size (5MB)",
      });
    }

    // A Buffer is a built-in Node.js object used to handle binary data (like files, images, PDFs, audio, etc.) in memory.
    // Unlike a string (text), a Buffer stores raw bytes directly.
    // Binary data = raw sequences of bytes (0s and 1s) that represent any kind of data on a computer.
    const dataBuffer = fs.readFileSync(resume.path); //Loads the PDF file from disk into memory as a Node.js Buffer
    const pdfData = await pdf(dataBuffer); //extracts raw text from the PDF and returns an object

    const prompt = `Review the following resume and provide constructive feeback on its strengths, weaknesses, and areas for improvement. Resume Content:\n\n${pdfData.text}`;

    const response = await AI.chat.completions.create({
      model: "gemini-2.0-flash",
      messages: [
        {
          role: "user",
          content: prompt,
        },
      ],
      temperature: 0.7,
      max_tokens: 1000,
    });

    const content = response.choices[0].message.content;

    await sql`INSERT INTO creations (user_id, prompt, content, type)
    VALUES (${userId}, 'Review the uploaded resume', ${content}, 'resume-review')`;

    res.json({ success: true, content });
  } catch (error) {
    console.log(error.message);
    res.json({ success: false, message: error.message });
  }
};
